        "($(typeof(error)))")
end

@inline function _command_slot_key(endpoint::PreparedCommandEndpoint,
    metadata::_CommandSlotMetadata)
    return PlantCommandOrderKey(metadata.scheduled_timestamp,
        PlantCommandSequence(metadata.sequence, _PLANT_COMMAND_COUNTER_TOKEN),
        endpoint.ordinal)
end

function next_command_order_key(endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState)
    _require_command_endpoint_binding(endpoint, state)
    iszero(state.pending_count) && return nothing
    slot = Int(state.calendar[1])
    return _command_slot_key(endpoint, state.slots[slot])
end

@inline function _should_supersede_pending_command(
    endpoint::PreparedCommandEndpoint,
    metadata::_CommandSlotMetadata,
    sequence::PlantCommandSequence)
    policy = endpoint.schema.effective_time_policy.supersession
    policy == SupersedeOlderPendingCommands || return false
    return metadata.status == _PendingCommandSlot &&
        metadata.sequence < sequence.value
end

function _superseded_pending_commands(endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState, sequence::PlantCommandSequence)
    count = 0
    first_slot = UInt32(0)
    @inbounds for index in 1:state.pending_count
        slot = state.calendar[index]
        metadata = state.slots[Int(slot)]
        _should_supersede_pending_command(endpoint, metadata, sequence) ||
            continue
        count += 1
        iszero(first_slot) && (first_slot = slot)
    end
    return count, first_slot
end

function _find_free_command_slot(state::CommandEndpointState)
    @inbounds for index in eachindex(state.slots)
        state.slots[index].status == _FreeCommandSlot && return UInt32(index)
    end
    _command_admission_error(:endpoint, :storage_invariant,
        "command endpoint has no free payload slot despite available capacity")
end

@inline function _presentation_range_clipping(schema::PlantCommandSchema)
    policy = schema.value_policy
    return policy.range_stage == ValidateOnPresentation &&
        policy.out_of_range == ClipInvalidCommand
end

@inline function _stage_scalar_command_payload(payload,
    ::UnboundedCommandValues, ::Bool)
    return payload
end

@inline function _stage_scalar_command_payload(payload,
    bounds::UniformCommandBounds, clip_values::Bool)
    return clip_values ? clamp(payload, bounds.lower, bounds.upper) : payload
end

@inline function _stage_command_payload!(
    ::_ScalarCommandPayloadSlots,
    schema::PlantCommandSchema,
    payload)
    return _stage_scalar_command_payload(payload, schema.bounds,
        _presentation_range_clipping(schema))
end

function _stage_command_payload!(payloads::_ArrayCommandPayloadSlots,
    schema::PlantCommandSchema, payload::AbstractArray)
    staging = payloads.values[payloads.staging_slot]
    copyto!(staging, payload)
    _clip_staged_command_payload!(staging, schema.bounds,
        _presentation_range_clipping(schema))
    return nothing
end

@inline _clip_staged_command_payload!(::AbstractArray,
    ::UnboundedCommandValues, ::Bool) = nothing

@inline function _clip_staged_command_payload!(staging::AbstractArray,
    bounds::UniformCommandBounds, clip_values::Bool)
    clip_values && clamp_array!(staging, bounds.lower, bounds.upper)
    return nothing
end

@inline function _commit_staged_command_payload!(
    payloads::_ScalarCommandPayloadSlots, slot::Int, staged)
    payloads.values[slot] = staged
    return nothing
end

@inline function _commit_staged_command_payload!(
    payloads::_ArrayCommandPayloadSlots, slot::Int, ::Nothing)
    staging_slot = payloads.staging_slot
    destination = payloads.values[slot]
    payloads.values[slot] = payloads.values[staging_slot]
    payloads.values[staging_slot] = destination
    return nothing
end


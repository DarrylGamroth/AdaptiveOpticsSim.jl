@inline function _next_command_counter(value::UInt64, reason::Symbol,
    label::AbstractString)
    value != typemax(UInt64) || _command_admission_error(:endpoint, reason,
        "$label exceeds UInt64 range")
    return value + UInt64(1)
end

@inline function _command_sequence_history_slot(sequence::UInt64,
    window::Int)
    return Int(mod(sequence - UInt64(1), UInt64(window))) + 1
end

function _classify_command_sequence(endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState, sequence::PlantCommandSequence)
    state.has_highest_sequence || return InOrderCommandSequence
    value = sequence.value
    slot = _command_sequence_history_slot(value, endpoint.sequence_window)
    state.accepted_sequences[slot] == value &&
        return DuplicateCommandSequence
    highest = state.highest_sequence
    if value > highest
        return value == highest + UInt64(1) ?
            InOrderCommandSequence : SkippedCommandSequence
    end
    highest - value >= UInt64(endpoint.sequence_window) &&
        return StaleCommandSequence
    return ReorderedCommandSequence
end

@inline function _record_accepted_command_sequence!(
    endpoint::PreparedCommandEndpoint, state::CommandEndpointState,
    sequence::PlantCommandSequence)
    value = sequence.value
    slot = _command_sequence_history_slot(value, endpoint.sequence_window)
    state.accepted_sequences[slot] = value
    if !state.has_highest_sequence || value > state.highest_sequence
        state.highest_sequence = value
        state.has_highest_sequence = true
    end
    return nothing
end

@inline function _command_sequence_action(policy::CommandSequencePolicy,
    sequence_class::CommandSequenceClass)
    sequence_class == InOrderCommandSequence && return AcceptSequence
    sequence_class == DuplicateCommandSequence && return policy.duplicate
    sequence_class == StaleCommandSequence && return policy.stale
    sequence_class == ReorderedCommandSequence && return policy.reordered
    return policy.skipped
end

@inline function _command_sequence_reason(
    sequence_class::CommandSequenceClass)
    sequence_class == DuplicateCommandSequence &&
        return CommandDispositionReason(:duplicate_sequence)
    sequence_class == StaleCommandSequence &&
        return CommandDispositionReason(:stale_sequence)
    sequence_class == ReorderedCommandSequence &&
        return CommandDispositionReason(:reordered_sequence)
    sequence_class == SkippedCommandSequence &&
        return CommandDispositionReason(:skipped_sequence)
    return CommandDispositionReason(:in_order_sequence)
end

"""
    validate_plant_command(endpoint, command)

Non-mutating validation of endpoint/schema identity and exact payload
compatibility. It does not assign a presentation credit, classify sequence,
admit, copy, schedule, supersede, claim, apply, or emit a disposition.
"""
function validate_plant_command(endpoint::PreparedCommandEndpoint,
    command::PlantCommand)
    command.endpoint == command_endpoint_id(endpoint) ||
        _command_admission_error(:validation, :endpoint_mismatch,
            "plant command targets $(command.endpoint); expected " *
            "$(command_endpoint_id(endpoint))")
    command.schema_id == command_schema_id(endpoint.schema) ||
        _command_admission_error(:validation, :schema_mismatch,
            "plant command schema $(command.schema_id) does not match " *
            "$(command_schema_id(endpoint.schema))")
    command.schema_version == command_schema_version(endpoint.schema) ||
        _command_admission_error(:validation, :schema_version_mismatch,
            "plant command schema version $(command.schema_version) does not " *
            "match $(command_schema_version(endpoint.schema))")
    validate_plant_command_payload(endpoint.schema, command.payload)
    return command
end

@inline function _command_lateness(timestamp::PlantTimestamp,
    requested::PlantTimestamp)
    requested < timestamp || return zero(PlantDuration)
    return timestamp - requested
end


end

function _write_superseded_dispositions!(
    workspace::CommandDispositionWorkspace,
    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    sequence::PlantCommandSequence,
    timestamp::PlantTimestamp,
    superseding_presentation::CommandPresentationID)
    count = 0
    @inbounds for index in 1:state.pending_count
        slot = Int(state.calendar[index])
        metadata = state.slots[slot]
        _should_supersede_pending_command(endpoint, metadata, sequence) ||
            continue
        count += 1
        workspace.dispositions[count] = _command_disposition(
            CommandPresentationID(metadata.presentation,
                _PLANT_COMMAND_COUNTER_TOKEN),
            endpoint,
            PlantCommandSequence(metadata.sequence,
                _PLANT_COMMAND_COUNTER_TOKEN),
            SupersededCommand,
            CommandDispositionReason(:superseded_by_newer_sequence),
            metadata.requested_effective_timestamp,
            timestamp;
            superseding_presentation=superseding_presentation.value,
        )
    end
    return count
end

function _remove_superseded_pending_commands!(
    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    sequence::PlantCommandSequence)
    write_index = 0
    removed = 0
    @inbounds for read_index in 1:state.pending_count
        slot = Int(state.calendar[read_index])
        metadata = state.slots[slot]
        if _should_supersede_pending_command(endpoint, metadata, sequence)
            state.slots[slot] = _CommandSlotMetadata(
                UInt64(0), UInt64(0), zero(PlantTimestamp),
                zero(PlantTimestamp), zero(PlantTimestamp),
                metadata.generation, _FreeCommandSlot)
            removed += 1
        else
            write_index += 1
            state.calendar[write_index] = UInt32(slot)
        end
    end
    state.pending_count = write_index
    state.active_count -= removed
    return removed
end

function _insert_pending_command_slot!(endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState, slot::UInt32)
    key = _command_slot_key(endpoint, state.slots[Int(slot)])
    insertion = state.pending_count + 1
    @inbounds while insertion > 1
        previous_slot = state.calendar[insertion - 1]
        previous_key = _command_slot_key(endpoint,
            state.slots[Int(previous_slot)])
        isless(key, previous_key) || break
        state.calendar[insertion] = previous_slot
        insertion -= 1
    end
    state.calendar[insertion] = slot
    state.pending_count += 1
    return key
end

@inline function _terminal_sequence_admission!(
    workspace::CommandDispositionWorkspace,
    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    command::PlantCommand,
    timestamp::PlantTimestamp,
    presentation_id::CommandPresentationID,
    sequence_class::CommandSequenceClass,
    action::CommandSequenceAction)
    reason = _command_sequence_reason(sequence_class)
    if action == FailOnSequence
        return _finish_failed_admission!(workspace, endpoint, state, command,
            timestamp, presentation_id, reason, sequence_class,
            reason.name,
            "plant-command sequence policy requires structural failure")
    end
    return _finish_terminal_admission!(workspace, endpoint, state, command,
        timestamp, presentation_id, RejectedCommand, reason, sequence_class)
end

"""
    admit_plant_command!(workspace, endpoint, state, command, timestamp)

Validate and make one bounded single-writer admission decision at canonical
plant `timestamp`. Successful admission copies caller payload storage, records
the accepted sequence, and inserts one sorted pending slot. Ordinary rejection
returns `CommandTerminatedOnAdmission`; structural fail policies first publish
one `FailedCommand` disposition and then throw `PlantCommandError`.

Decision precedence is endpoint/schema/payload validation, sequence policy,
effective-time policy, supersession/capacity, and finally transactional payload
storage. A failure at an earlier stage never falls through to a later reason.

The first successfully admitted sequence establishes the endpoint baseline.
Only successful admissions enter the bounded accepted-sequence window. A
command rejected for time or capacity may therefore be retried with the same
endpoint sequence as a distinct presentation.
"""
function admit_plant_command!(workspace::CommandDispositionWorkspace,
    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    command::PlantCommand,
    timestamp::PlantTimestamp)
    _require_command_endpoint_binding(endpoint, state)
    _require_command_endpoint_binding(endpoint, workspace)
    _require_empty_command_dispositions(workspace)
    _require_idle_command_endpoint(state)
    _require_forward_command_timestamp(state, timestamp)
    presentation_id = _next_command_presentation(state)

    try
        validate_plant_command(endpoint, command)
    catch error
        return _handle_command_validation_error!(workspace, endpoint, state,
            command, timestamp, presentation_id, error)
    end

    sequence_class = _classify_command_sequence(endpoint, state,
        command.sequence)
    sequence_action = _command_sequence_action(
        endpoint.schema.sequence_policy, sequence_class)
    sequence_action == AcceptSequence || return _terminal_sequence_admission!(
        workspace, endpoint, state, command, timestamp, presentation_id,
        sequence_class, sequence_action)

    requested = command.effective_timestamp
    scheduled = requested
    if timestamp < requested
        endpoint.schema.effective_time_policy.future == RejectFutureCommand &&
            return _finish_terminal_admission!(workspace, endpoint, state,
                command, timestamp, presentation_id, RejectedCommand,
                CommandDispositionReason(:future_command), sequence_class)
    elseif requested < timestamp
        late_policy = endpoint.schema.effective_time_policy.late
        if late_policy == RejectLateCommand
            return _finish_terminal_admission!(workspace, endpoint, state,
                command,
                timestamp, presentation_id, RejectedCommand,
                CommandDispositionReason(:late_command), sequence_class)
        elseif late_policy == FailOnLateCommand
            return _finish_failed_admission!(workspace, endpoint, state,
                command, timestamp, presentation_id,
                CommandDispositionReason(:late_command), sequence_class,
                :late_command,
                "late plant-command policy requires structural failure")
        end
        scheduled = timestamp
    end

    superseded_count, first_superseded_slot =
        _superseded_pending_commands(endpoint, state, command.sequence)
    resulting_active = state.active_count - superseded_count + 1
    if resulting_active > endpoint.capacity
        return _finish_terminal_admission!(workspace, endpoint, state, command,
            timestamp, presentation_id, RejectedCommand,
            CommandDispositionReason(:calendar_capacity), sequence_class)
    end

    slot = iszero(first_superseded_slot) ?
        _find_free_command_slot(state) : first_superseded_slot
    old_metadata = state.slots[Int(slot)]
    generation = _next_command_counter(old_metadata.generation,
        :slot_generation_overflow, "command payload-slot generation")

    staged = try
        _stage_command_payload!(state.payloads, endpoint.schema,
            command.payload)
    catch error
        return _handle_command_staging_error!(workspace, endpoint, state,
            command, timestamp, presentation_id, sequence_class, error)
    end

    disposition_count = _write_superseded_dispositions!(workspace, endpoint,
        state, command.sequence, timestamp, presentation_id)
    removed = _remove_superseded_pending_commands!(endpoint, state,
        command.sequence)
    removed == superseded_count || _command_admission_error(:endpoint,
        :storage_invariant,
        "command supersession preflight did not match calendar mutation")

    _commit_staged_command_payload!(state.payloads, Int(slot), staged)
    state.slots[Int(slot)] = _CommandSlotMetadata(
        presentation_id.value,
        command.sequence.value,
        requested,
        scheduled,
        timestamp,
        generation,
        _PendingCommandSlot,
    )
    key = _insert_pending_command_slot!(endpoint, state, slot)
    state.active_count += 1
    _record_accepted_command_sequence!(endpoint, state, command.sequence)
    state.presentation_sequence = presentation_id.value
    state.current_timestamp = timestamp
    workspace.count = disposition_count
    status = scheduled <= timestamp ?
        CommandAdmittedReady : CommandAdmittedPending
    return PlantCommandAdmission(presentation_id, status, sequence_class, key)
end

struct PlantCommandApplicationClaim
    binding_id::UInt64
    slot::UInt32
    generation::UInt64
    presentation_id::CommandPresentationID
    key::PlantCommandOrderKey
    requested_effective_timestamp::PlantTimestamp
    admission_timestamp::PlantTimestamp
    ready_timestamp::PlantTimestamp
end

@inline command_presentation_id(value::PlantCommandApplicationClaim) =
    value.presentation_id
@inline command_order_key(value::PlantCommandApplicationClaim) = value.key
@inline command_sequence(value::PlantCommandApplicationClaim) =
    value.key.sequence
@inline command_requested_effective_timestamp(
    value::PlantCommandApplicationClaim) = value.requested_effective_timestamp
@inline command_admission_timestamp(value::PlantCommandApplicationClaim) =
    value.admission_timestamp
@inline command_application_timestamp(value::PlantCommandApplicationClaim) =
    value.ready_timestamp
@inline command_lateness(value::PlantCommandApplicationClaim) =
    _command_lateness(value.ready_timestamp,
        value.requested_effective_timestamp)

"""
    claim_next_application_ready_command!(endpoint, state, timestamp)

Remove and claim the earliest pending command whose scheduled time is not
later than `timestamp`. At most one application claim may be outstanding. A
late drain records `timestamp` as the actual application-ready instant; it
never backdates plant state to the scheduled key.
"""
function claim_next_application_ready_command!(
    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    timestamp::PlantTimestamp)
    _require_command_endpoint_binding(endpoint, state)
    _require_idle_command_endpoint(state)
    _require_forward_command_timestamp(state, timestamp)
    state.current_timestamp = timestamp
    iszero(state.pending_count) && return nothing

    slot = state.calendar[1]
    metadata = state.slots[Int(slot)]
    metadata.scheduled_timestamp <= timestamp || return nothing
    @inbounds for index in 2:state.pending_count
        state.calendar[index - 1] = state.calendar[index]
    end
    state.pending_count -= 1
    state.slots[Int(slot)] = _CommandSlotMetadata(
        metadata.presentation,
        metadata.sequence,
        metadata.requested_effective_timestamp,
        metadata.scheduled_timestamp,
        metadata.admission_timestamp,
        metadata.generation,
        _ApplicationReadyCommandSlot,
    )
    state.outstanding_slot = slot
    return PlantCommandApplicationClaim(
        _command_endpoint_binding_id(endpoint),
        slot,
        metadata.generation,
        CommandPresentationID(metadata.presentation,
            _PLANT_COMMAND_COUNTER_TOKEN),
        _command_slot_key(endpoint, metadata),
        metadata.requested_effective_timestamp,
        metadata.admission_timestamp,
        timestamp,
    )
end

@inline function _require_current_command_claim(
    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    claim::PlantCommandApplicationClaim)
    _require_command_endpoint_binding(endpoint, state)
    _command_endpoint_binding_id(endpoint) == claim.binding_id ||
        _command_admission_error(:application, :foreign_claim,
            "application-ready command belongs to another endpoint")
    state.outstanding_slot == claim.slot || _command_admission_error(
        :application, :stale_claim,
        "application-ready command is stale or no longer outstanding")
    slot = Int(claim.slot)
    1 <= slot <= length(state.slots) || _command_admission_error(
        :application, :invalid_claim,
        "application-ready command contains an invalid payload slot")
    metadata = state.slots[slot]
    metadata.status == _ApplicationReadyCommandSlot &&
        metadata.generation == claim.generation &&
        metadata.presentation == claim.presentation_id.value ||
        _command_admission_error(:application, :stale_claim,
            "application-ready command no longer matches endpoint state")
    return slot, metadata
end

"""Borrow the endpoint-owned payload for the outstanding claim read-only."""
function claimed_command_payload(endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState, claim::PlantCommandApplicationClaim)
    slot, _ = _require_current_command_claim(endpoint, state, claim)
    return _claimed_command_payload(state.payloads, slot)
end

@inline _claimed_command_payload(payloads::_ScalarCommandPayloadSlots,
    slot::Int) = payloads.values[slot]
@inline _claimed_command_payload(payloads::_ArrayCommandPayloadSlots,
    slot::Int) = payloads.values[slot]

@inline function _release_claimed_command_slot!(state::CommandEndpointState,
    slot::Int, metadata::_CommandSlotMetadata)
    state.slots[slot] = _CommandSlotMetadata(
        UInt64(0), UInt64(0), zero(PlantTimestamp), zero(PlantTimestamp),
        zero(PlantTimestamp), metadata.generation, _FreeCommandSlot)
    state.active_count -= 1
    state.outstanding_slot = UInt32(0)
    return nothing
end

function _finish_command_application!(
    workspace::CommandDispositionWorkspace,
    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    claim::PlantCommandApplicationClaim,
    kind::CommandTerminalKind,
    reason::CommandDispositionReason)
    _require_command_endpoint_binding(endpoint, workspace)
    _require_empty_command_dispositions(workspace)
    slot, metadata = _require_current_command_claim(endpoint, state, claim)
    disposition = _command_disposition(claim.presentation_id, endpoint,
        claim.key.sequence, kind, reason,
        metadata.requested_effective_timestamp, claim.ready_timestamp)
    workspace.dispositions[1] = disposition
    workspace.count = 1
    _release_claimed_command_slot!(state, slot, metadata)
    return disposition
end

"""
Record successful application after the caller has applied the borrowed
payload. This transition records the terminal credit and releases the slot; it
does not itself mutate an optic.
"""
function mark_plant_command_applied!(
    workspace::CommandDispositionWorkspace,
    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    claim::PlantCommandApplicationClaim)
    return _finish_command_application!(workspace, endpoint, state, claim,
        AppliedCommand, CommandDispositionReason(:applied))
end

"""Record a declared application failure and release the claimed slot."""
function fail_plant_command_application!(
    workspace::CommandDispositionWorkspace,
    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    claim::PlantCommandApplicationClaim;
    reason=CommandDispositionReason(:application_failure))
    resolved_reason = _as_command_disposition_reason(reason)
    return _finish_command_application!(workspace, endpoint, state, claim,
        FailedCommand, resolved_reason)
end

"""
    fail_pending_plant_commands!(workspace, endpoint, state, timestamp;
        reason=:endpoint_failure)

Boundedly terminate every pending command in calendar order. This is the core
failure/drain primitive for commands that have not become application-ready;
an outstanding application claim must be resolved separately.
"""
function fail_pending_plant_commands!(
    workspace::CommandDispositionWorkspace,
    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    timestamp::PlantTimestamp;
    reason=CommandDispositionReason(:endpoint_failure))
    _require_command_endpoint_binding(endpoint, state)
    _require_command_endpoint_binding(endpoint, workspace)
    _require_empty_command_dispositions(workspace)
    _require_idle_command_endpoint(state)
    _require_forward_command_timestamp(state, timestamp)
    resolved_reason = _as_command_disposition_reason(reason)
    count = state.pending_count
    @inbounds for index in 1:count
        slot = Int(state.calendar[index])
        metadata = state.slots[slot]
        workspace.dispositions[index] = _command_disposition(
            CommandPresentationID(metadata.presentation,
                _PLANT_COMMAND_COUNTER_TOKEN),
            endpoint,
            PlantCommandSequence(metadata.sequence,
                _PLANT_COMMAND_COUNTER_TOKEN),
            FailedCommand,
            resolved_reason,
            metadata.requested_effective_timestamp,
            timestamp,
        )
        state.slots[slot] = _CommandSlotMetadata(
            UInt64(0), UInt64(0), zero(PlantTimestamp),
            zero(PlantTimestamp), zero(PlantTimestamp), metadata.generation,
            _FreeCommandSlot)
    end
    state.pending_count = 0
    state.active_count = 0
    state.current_timestamp = timestamp
    workspace.count = count
    return count
end

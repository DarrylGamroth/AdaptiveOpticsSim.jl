#
# Bounded single-writer plant-command admission
#
# This layer owns copied command payloads, accepted-sequence history, a
# fixed-capacity future calendar, application-ready claims, and terminal model
# dispositions. It deliberately does not mutate an optic, model command
# silence, compose plant event phases, or define a HIL port or transport.
#

@inline function _command_admission_error(stage::Symbol, reason::Symbol,
    message::AbstractString)
    throw(PlantCommandError(stage, reason, String(message)))
end


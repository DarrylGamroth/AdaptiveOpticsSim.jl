"""
    prepare_command_endpoint(schema; capacity, sequence_window=capacity,
        ordinal, backend=CPUBackend())

Prepare immutable bounds and storage policy for one command endpoint. Creating
its separate `CommandEndpointState` allocates exactly `capacity` active payload
slots plus one transactional staging slot for array payloads. No run-length-
sized storage, optic mutation, callback, clock, queue, or transport is added.
"""
function prepare_command_endpoint(schema::PlantCommandSchema;
    capacity,
    sequence_window=capacity,
    ordinal,
    backend::AbstractArrayBackend=CPUBackend())
    checked_capacity = _checked_command_endpoint_capacity(capacity)
    checked_window = _checked_command_sequence_window(sequence_window)
    checked_ordinal = _checked_command_endpoint_ordinal(ordinal)
    _validate_command_supersession_semantics(schema)
    _validate_command_storage_backend(schema, backend)
    return PreparedCommandEndpoint(_CommandEndpointBinding(), schema,
        checked_capacity, checked_window, checked_ordinal, backend)
end

abstract type _AbstractCommandPayloadSlots end

struct _ScalarCommandPayloadSlots{T} <: _AbstractCommandPayloadSlots
    values::Memory{T}
end

struct _ArrayCommandPayloadSlots{A<:AbstractArray} <:
    _AbstractCommandPayloadSlots
    values::Memory{A}
    staging_slot::Int
end

function _prepare_command_payload_slots(schema::PlantCommandSchema{T,0},
    ::AbstractArrayBackend, capacity::Int) where {T}
    return _ScalarCommandPayloadSlots(Memory{T}(undef, capacity))
end

function _prepare_command_payload_slots(schema::PlantCommandSchema{T,N},
    backend::AbstractArrayBackend, capacity::Int) where {T,N}
    count = capacity + 1
    first_slot = allocate_array(backend, T, schema.dimensions...)
    values = Memory{typeof(first_slot)}(undef, count)
    values[1] = first_slot
    @inbounds for index in 2:count
        values[index] = allocate_array(backend, T, schema.dimensions...)
    end
    return _ArrayCommandPayloadSlots(values, count)
end


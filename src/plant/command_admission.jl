    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    command::PlantCommand,
    timestamp::PlantTimestamp,
    presentation_id::CommandPresentationID,
    kind::CommandTerminalKind,
    reason::CommandDispositionReason,
    sequence_class::Union{Nothing,CommandSequenceClass})
    disposition = _command_disposition(presentation_id, endpoint,
        command.sequence, kind, reason, command.effective_timestamp,
        timestamp)
    workspace.dispositions[1] = disposition
    workspace.count = 1
    state.presentation_sequence = presentation_id.value
    state.current_timestamp = timestamp
    return PlantCommandAdmission(presentation_id,
        CommandTerminatedOnAdmission, sequence_class, nothing)
end

@noinline function _finish_failed_admission!(
    workspace::CommandDispositionWorkspace,
    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    command::PlantCommand,
    timestamp::PlantTimestamp,
    presentation_id::CommandPresentationID,
    reason::CommandDispositionReason,
    sequence_class::Union{Nothing,CommandSequenceClass},
    error_reason::Symbol,
    message::AbstractString)
    _finish_terminal_admission!(workspace, endpoint, state, command,
        timestamp, presentation_id, FailedCommand, reason, sequence_class)
    _command_admission_error(:admission, error_reason, message)
end

@inline function _validation_terminal_kind(error::PlantCommandError)
    error.reason === :nonfinite_failure && return FailedCommand
    error.reason === :out_of_range_failure && return FailedCommand
    return RejectedCommand
end

@noinline function _handle_command_validation_error!(
    workspace::CommandDispositionWorkspace,
    endpoint::PreparedCommandEndpoint,
    state::CommandEndpointState,
    command::PlantCommand,
    timestamp::PlantTimestamp,
    presentation_id::CommandPresentationID,
    error::PlantCommandError)
    kind = _validation_terminal_kind(error)
    reason = CommandDispositionReason(error.reason)
    if kind == FailedCommand
        return _finish_failed_admission!(workspace, endpoint, state, command,
            timestamp, presentation_id, reason, nothing, error.reason,
            error.msg)
    end
    return _finish_terminal_admission!(workspace, endpoint, state, command,
        timestamp, presentation_id, RejectedCommand, reason, nothing)
end

